-- Recursively reconstructs a lua userdata object, given a table that was serialized with userdata_structure_to_table, and a constructor
function userdata_from_table(tab, constructor)
    local ud = constructor()

    function set_deep(keys, val)
        local v = ud

        -- pop the last key beacuse we need to set it by indexing the original table
        local last_key = table.remove(keys)

        for i, key in ipairs(keys) do
            -- hooray for references
            v = v[key]
        end

        v[last_key] = val
    end

    -- recursively go down, looking for tables, and populating their respective userdata objects, builds up a list of keys, i.e. {"pos", "x"}
    function traverse(sub_table, keys)
        for k, v in pairs(sub_table) do
            local new_keys = {unpack(keys)} -- clone
            table.insert(new_keys, k)

            if type(v) == 'table' then
                traverse(v, new_keys)
            else
                set_deep(new_keys, v)
            end
        end
    end

    traverse(tab, {})

    return ud
end

function add_to_current_scene(source)
    local current_scene_source = obs.obs_frontend_get_current_scene()
    obs.obs_scene_add(obs.obs_scene_from_source(current_scene_source), source)
    obs.obs_source_release(current_scene_source)
end

-- Create a sceneitem (and its required source) based on a table containing sceneitem data, and a scene to make it in
function create_sceneitem(item_data, scene)
    local source = create_source(item_data.source_data)
    local sceneitem = obs.obs_scene_add(scene, source)

    local transform_info = userdata_from_table(item_data.transform_info, obs.obs_transform_info)
    local crop = userdata_from_table(item_data.crop, obs.obs_sceneitem_crop)

    obs.obs_sceneitem_set_info(sceneitem, transform_info)
    obs.obs_sceneitem_set_crop(sceneitem, crop)
    obs.obs_sceneitem_set_visible(sceneitem, item_data.is_visible)

    obs.obs_source_release(source)

    return sceneitem
end


function setup_sceneitems(sceneitems, source)
    local scene = obs.obs_scene_from_source(source)

    for i, item_data in ipairs(sceneitems) do
        create_sceneitem(item_data, scene)
    end
end

function setup_group_sceneitems(sceneitems, scene, group)
    obs.obs_sceneitem_defer_group_resize_begin(group)

    for i, item_data in ipairs(sceneitems) do
        local sceneitem = create_sceneitem(item_data, scene)
        obs.obs_sceneitem_group_add_item(group, sceneitem)
    end

    obs.obs_sceneitem_defer_group_resize_end(group)
end

function replace(str, what, with)
    what = string.gsub(what, "[%(%)%.%+%-%*%?%[%]%^%$%%]", "%%%1") -- escape pattern
    with = string.gsub(with, "[%%]", "%%%%") -- escape replacement
    return string.gsub(str, what, with)
end

function last_index_of(haystack, needle)
    local last_index = 0
    needle = string.gsub(needle, "[%(%)%.%+%-%*%?%[%]%^%$%%]", "%%%1")

    while haystack:sub(last_index+1, haystack:len()):find(needle) ~= nil do
        last_index = last_index + haystack:sub(last_index+1, haystack:len()):find(needle)
    end

    return last_index
end

function serialize(o)
    if type(o) == 'table' then
       local s = '{ '
       for k,v in pairs(o) do
          if type(k) ~= 'number' then k = '"'..k..'"' end
          s = s .. '['..k..'] = ' .. serialize(v) .. ','
       end
       return s .. '} '
    elseif type(o) == 'string' then
        return ("%q"):format(o)
    else
       return tostring(o)
    end
end


function create_source(source_data) 
    -- Substitute things in for absolute paths
    local absolute_path_base = script_path()

    if META.subfolder ~= nil and META.subfolder ~= "" then
        local subfolder_index = last_index_of(absolute_path_base, META.subfolder)
        absolute_path_base = absolute_path_base:sub(0, subfolder_index)
    end

    local settings_json = replace(source_data.settings_json, "{{!PATH!}}", absolute_path_base)

    local new_source_settings = obs.obs_data_create_from_json(settings_json)

    local new_source = obs.obs_source_create(source_data.id, source_data.name, new_source_settings, nil)

    if type(source_data.filters) == 'table' then
        for i, filter_data in ipairs(source_data.filters) do
            local filter_source = create_source(filter_data)
            obs.obs_source_filter_add(new_source, filter_source)
            obs.obs_source_release(filter_source)
        end
    end

    if source_data.id == 'scene' then
        setup_sceneitems(source_data.sceneitems, new_source)
    elseif source_data.id == 'group' then
        -- todo use the api function when next obs studio version is released and obs_group_from_source is exposed
        -- for now we get this jank :)
        local private_scene = obs.obs_scene_create_private("group_import_scene")
        local group_sceneitem = obs.obs_scene_add(private_scene, new_source)
        local private_scene_source = obs.obs_scene_get_source(private_scene)

        setup_group_sceneitems(source_data.sceneitems, private_scene, group_sceneitem)

        -- obs.obs_sceneitem_addref(group_sceneitem)
        -- obs.obs_sceneitem_remove(group_sceneitem)
        -- obs.obs_source_remove(private_scene_source)
        obs.obs_scene_release(private_scene)
    end

    obs.obs_data_release(new_source_settings)

    return new_source
end

function import_scene_template(scene_data, auto_switch)
    auto_switch = auto_switch or false

    local scene_as_source = create_source(scene_data)

    if auto_switch then
        obs.obs_frontend_set_current_scene(scene_as_source)
    end

    return scene_as_source
end

function scene_list_release(list)
    for i, scene in ipairs(list) do
        obs.obs_source_release(scene)
    end
end

function is_fresh_scene_collection()
    local scene_names = obs.obs_frontend_get_scene_names()
    local ret = false
    
    if table.getn(scene_names) == 1 and scene_names[1] == "Scene" then
        local scene_list = obs.obs_frontend_get_scenes()
        local scene = scene_list[1]

        local sceneitems = obs.obs_scene_enum_items(obs.obs_scene_from_source(scene))

        if sceneitems == nil or table.getn(sceneitems) == 0 then
            ret = true 
        end

        scene_list_release(scene_list)
        obs.sceneitem_list_release(sceneitems)
    end

    return ret
end

function import_all_scene_templates()
    local is_fresh = is_fresh_scene_collection()

    for i, scene_data in ipairs(SCENES) do
        local should_switch = i == 1
        local scene_as_source = import_scene_template(scene_data, should_switch)
        obs.obs_source_release(scene_as_source)
    end

    if is_fresh then
        local scene_list = obs.obs_frontend_get_scenes()
        local first_scene = scene_list[1]

        obs.obs_source_remove(first_scene)
        scene_list_release(scene_list)
    end
end

