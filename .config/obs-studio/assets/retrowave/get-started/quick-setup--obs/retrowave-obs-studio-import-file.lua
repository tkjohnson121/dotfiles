PropertyGroup = {}

function PropertyGroup:new(name, id, properties)
    o = { name = name, id = id, properties = properties }
    setmetatable(o, self)
    self.__index = self
    return o
end

function PropertyGroup:push(property)
    table.insert(self.properties, property)
end

function PropertyGroup:add_to_list_prop(list_prop)
    obs.obs_property_list_add_string(list_prop, self.name, self.id)
end

function PropertyGroup:hide()
    if self.properties ~= nil then
        for index, prop in ipairs(self.properties) do
            obs.obs_property_set_visible(prop, false)
        end
    end
end

function PropertyGroup:show()
    if self.properties ~= nil then
        for index, prop in ipairs(self.properties) do
            obs.obs_property_set_visible(prop, true)
        end
    end
end

PropertyGroupsController = {}

function PropertyGroupsController:new(parent_props, name, id, create_groups)
    self.groups_list_prop = obs.obs_properties_add_list(parent_props, id, name, obs.OBS_COMBO_TYPE_LIST, obs.OBS_COMBO_FORMAT_STRING)
    self.groups = create_groups()
    self.id = id

    self.active_group_id = self.groups[1].id
    self.last_active_id = self.groups[1].id

    obs.obs_property_set_modified_callback(self.groups_list_prop, function(props, prop, settings) return self:on_change(props, prop, settings) end)

    for i, v in ipairs(self.groups) do
        v:add_to_list_prop(self.groups_list_prop)

        if i ~= 1 then
            v:hide()
        end
    end

    return self
end

function PropertyGroupsController:find_group_by_id(id)
    for i, group in ipairs(self.groups) do
        if group.id == id then
            return group
        end
    end

    return nil
end

function PropertyGroupsController:on_change(props, prop, settings)
    new_active = obs.obs_data_get_string(settings, self.id)

    if new_active == "" then
        return true
    end

    if new_active ~= self.active_group_id then
        self:set_active(new_active)
    end

    return true
end

function PropertyGroupsController:get_active()
    return self:find_group_by_id(self.active_group_id)
end

function PropertyGroupsController:set_active(id)
    self:get_active():hide()
    self.active_group_id = id
    self:get_active():show()
end

function script_properties()
    local script_props = obs.obs_properties_create()
    
    function create_groups()
        -- factories for creating button callbacks
        function add_scene_callback_factory(scene_data)
            return function()
                local scene_as_source = import_scene_template(scene_data)
                obs.obs_source_release(scene_as_source)
            end
        end

        function add_source_callback_factory(source_data)
            return function()
                local source = create_source(source_data)
                add_to_current_scene(source)
                obs.obs_source_release(source)
            end
        end

        local scene_buttons = {}
        local source_buttons = {}

        local all_btn = obs.obs_properties_add_button(script_props, "add_all_scenes", "Setup all scenes", import_all_scene_templates)
        table.insert(scene_buttons, all_btn)

        for i, scene_data in ipairs(SCENES) do
            local btn = obs.obs_properties_add_button(script_props, "add_scene_" .. i, "Setup \"" .. scene_data.name .. "\" scene", add_scene_callback_factory(scene_data))
            table.insert(scene_buttons, btn)
        end

        for i, source_data in ipairs(SOURCES) do
            local btn = obs.obs_properties_add_button(script_props, "add_source_" .. i, "Add \"" .. source_data.name .. "\" source", add_source_callback_factory(source_data))
            table.insert(source_buttons, btn)
        end

        return {
            PropertyGroup:new("Scene Templates", "menu_scene_templates", scene_buttons),
            PropertyGroup:new("Sources", "menu_sources", source_buttons)
        }
    end

    local groups_ctrl = PropertyGroupsController:new(script_props, "Menu", "menu_list", create_groups)

    return script_props
end

